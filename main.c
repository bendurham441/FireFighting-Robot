void room_scan() {
	forward(63);
	wait1Msec(1000);
	while(fire == false) {
		int counter = 0;
		if(SensorValue[light] >= 20 && counter < 29) {
			rotate();
			counter += 1;
		} else {
			extinguish();
			if(SensorValue[light] <= 20) {
				rotate_arm();
				extinguish();
			} else {
				return;
			}
		}
	}
}

