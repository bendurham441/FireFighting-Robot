void forward(int speed){
	motor[right_motor] = 	speed;
	motor[left_motor] = speed;
}

Void back(int speed){
	motor[right_motor] = speed * -1;
	Motor[left_motor] = speed * -1;
}

void stop(){
	motor[right_motor] = 	0;
	motor[left_motor] = 0;
}

void turn_right() {
	motor[right_motor] = -63;
	motor[left_motor] = 63;
	wait1Msec(750);
}

void extinguish() {
	forward(63);
	wait1Msec(200);
	back(63);
	wait1Msec(200);
}

